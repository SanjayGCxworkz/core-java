public class RailwayStation{


private int stationCode;    
private String junctionName;
private int noOfPlatforms;
private int noOfStaff;
private String stationMaster;

	public void setStationCode(int stationCode){

		this.stationCode=stationCode;


	} 
	
	public int getStationCode(){
	
		return stationCode;
	}
	
	
	
	public void setJunctionName(String junctionName){
	
		this.junctionName=junctionName;
		
	
	}
	public String getJunctionName(){
	
		return junctionName;
	
	}
	
	public void setNoOfPlatforms(int noOfPlatforms){
	
		this.noOfPlatforms=noOfPlatforms;
		
	
	}
	public int getNoOfPlatforms(){
	
		return noOfPlatforms;
	
	}
	
	
	public void setNoOfStaff(int noOfStaff){

		this.noOfStaff=noOfStaff;


	} 
	
	public int getNoOfStaff(){
	
		return noOfStaff;
	}
	
	
	
	
	public void setStationMaster(String stationMaster){
	
		this.stationMaster=stationMaster;
	}
	public String getStationMaster(){
	
		return stationMaster;
	}

	
	




}